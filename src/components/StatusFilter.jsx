// ПОЧЕМУ? Компонент фильтрации вынесен отдельно, а не встроен в App?
// 1. Single Responsibility: компонент отвечает только за UI фильтрации,
//    не знает о том, как хранятся данные или как работает приложение.
// 2. Переиспользование: этот же фильтр можно использовать на других страницах
//    (например, в админ-панели или отчете).
// 3. Тестируемость: легче написать юнит-тесты на изолированный компонент.
export default function StatusFilter({ activeFilter, onFilterChange }) {
  // ПОЧЕМУ? Массив filters вынесен в константу внутри компонента?
  // 1. Централизация: если нужно добавить новый статус, меняем в одном месте.
  // 2. DRY (Don't Repeat Yourself): не дублируем кнопки вручную в JSX.
  // 3. Гибкость: легко изменить порядок или добавить условия отображения.
  const filters = ["Все", "Подтверждено", "Ожидание"];

  return (
    <div className="filter-panel">
      {/* ПОЧЕМУ? map для генерации кнопок, а не три отдельные кнопки?
          1. Масштабируемость: добавление нового фильтра — одна строка в массиве.
          2. Консистентность: все кнопки рендерятся одинаково, нет риска
             забыть добавить класс или обработчик на одну из кнопок.
          3. Меньше кода: вместо 15 строк JSX — 5 строк с map. */}
      {filters.map((filter) => (
        <button
          key={filter}
          className={`filter-panel__btn ${
            activeFilter === filter ? "filter-panel__btn--active" : ""
          }`}
          onClick={() => onFilterChange(filter)}
          // ПОЧЕМУ? aria-pressed для кнопок фильтра?
          // Это стандарт доступности (a11y) для toggle-кнопок.
          // Скринридеры сообщат пользователю, какая кнопка активна.
          aria-pressed={activeFilter === filter}
        >
          {filter}
        </button>
      ))}
    </div>
  );
}