// ServiceSearch.jsx
// ПОЧЕМУ? SearchBar — «глупый» (presentational) компонент: он не хранит state,
// а получает value и onChange от родителя. Это позволяет централизованно
// управлять фильтрацией в App и легко переиспользовать компонент.
// ПОЧЕМУ? Компонент назван ServiceSearch, чтобы отражать тему проекта
// (поиск по названию/адресу помещения), а не учебный поиск курсов.

// ПОЧЕМУ? Деструктуризация props в параметрах функции делает код чище
// и сразу показывает контракт компонента: какие данные он ожидает.
export default function ServiceSearch({ value, onChange }) {
  return (
    <div className="search-bar">
      {/* ПОЧЕМУ? value={value} делает поле «управляемым»: React контролирует
          значение инпута, что позволяет программно очищать/менять его.
          Без этого поле было бы «неуправляемым» и сложнее для тестирования. */}
      <input
        type="text"
        value={value}
        // ПОЧЕМУ? onChange={(e) => onChange(e.target.value)} — передаёт
        // новое значение наверх в родительский компонент. Это реализация
        // однонаправленного потока данных (data flows down, events flow up).
        onChange={(e) => onChange(e.target.value)}
        placeholder="Поиск помещения (например, Переговорная)..."
        className="search-bar__input"
        // ПОЧЕМУ? Ограничение maxLength={50} — защита от чрезмерного ввода,
        // которая предотвращает потенциальные проблемы с валидацией и производительностью.
        maxLength={50}
      />

      {/* ПОЧЕМУ? Кнопка очистки показывается только при непустом поле —
          это улучшает UX, скрывая нерелевантные элементы интерфейса. */}
      {value.length > 0 && (
        // ПОЧЕМУ? onClick={() => onChange('')} — сброс поиска через
        // передачу пустой строки в родитель. Компонент не меняет state сам,
        // соблюдая принцип «единственного источника правды» в родителе.
        <button
          type="button"
          onClick={() => onChange('')}
          className="search-bar__clear"
          aria-label="Очистить поиск"
        >
          ✕
        </button>
      )}

      {/* ПОЧЕМУ? Счётчик символов помогает пользователю ориентироваться
          в ограничениях ввода и демонстрирует работу с производными данными от state. */}
      <small className="search-bar__counter">
        Введено символов: {value.length}/50
      </small>
    </div>
  );
}