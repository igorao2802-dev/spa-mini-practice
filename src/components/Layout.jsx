// ПОЧЕМУ? Паттерн "компонент-обертка" (wrapper component) вместо дублирования header/footer?
// 1. DRY (Don't Repeat Yourself): если нужно изменить шапку или добавить навигацию,
//    меняем в одном месте (Layout), а не в каждом компоненте страницы.
// 2. Консистентность: все страницы имеют одинаковую структуру.
// 3. Масштабируемость: легко добавить sidebar, breadcrumbs или другие общие элементы.
export default function Layout({ title, children }) {
  return (
    <div className="layout">
      <header className="layout__header">
        <h1>{title}</h1>
        {/* ПОЧЕМУ? Подзаголовок вынесен в Layout, а не передается как prop?
            Это статический текст, который одинаков для всего приложения.
            Если бы подзаголовок менялся на разных страницах,
            мы бы передавали его как prop (subtitle). */}
        <p>Система онлайн-бронирования помещений</p>
      </header>

      {/* ПОЧЕМУ? children вместо явной передачи компонентов как props?
          1. Гибкость: Layout не знает и не контролирует, что внутри.
             Можно передать что угодно: форму, список, таблицу.
          2. Композиция: родительский компонент (App) решает,
             какие компоненты рендерить, Layout только предоставляет "слот".
          3. Чистота: не нужно объявлять props для каждого возможного ребенка.
          Это основа композиции в React — компоненты как черные ящики. */}
      <main className="layout__main">
        {children}
      </main>
    </div>
  );
}