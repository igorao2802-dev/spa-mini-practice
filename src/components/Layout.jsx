// GuestSelector.jsx
// ПОЧЕМУ? Компонент изолирован: его state не виден снаружи.
// Это называется инкапсуляция состояния — компонент сам управляет своей логикой.
import { useState } from "react";

// ПОЧЕМУ? Имя компонента с заглавной буквы — правило React для отличия от обычных функций.
// ПОЧЕМУ? Компонент назван GuestSelector, чтобы соответствовать теме бронирования
// (выбор количества участников мероприятия), а не абстрактного счётчика.
export default function GuestSelector() {
  // ПОЧЕМУ? useState(1) — начальное значение 1 гость, т.к. бронирование
  // минимум на одного человека. count — текущее значение, setGuestsCount — setter.
  const [guestsCount, setGuestsCount] = useState(1);

  // ПОЧЕМУ? Функция increment использует функциональное обновление setGuestsCount(prev => prev + 1),
  // чтобы гарантировать корректную работу при нескольких быстрых кликах (batching в React).
  function increment() {
    // ПОЧЕМУ? Проверка на максимум (10 гостей) — валидация на уровне UI,
    // чтобы предотвратить некорректные данные до отправки формы.
    setGuestsCount((prev) => (prev < 10 ? prev + 1 : prev));
  }

  // ПОЧЕМУ? Функция decrement также использует функциональное обновление
  // и защиту от значений меньше 1 — бизнес-правило «минимум один участник».
  function decrement() {
    setGuestsCount((prev) => (prev > 1 ? prev - 1 : prev));
  }

  // ПОЧЕМУ? Функция reset сбрасывает значение в 1 (не в 0), т.к.
  // бронирование на 0 гостей не имеет смысла в предметной области.
  function reset() {
    setGuestsCount(1);
  }

  return (
    <div className="counter">
      {/* ПОЧЕМУ? Условный рендеринг для грамматически верного текста:
          «гость» / «гостя» / «гостей» в зависимости от количества. */}
      <p className="counter__value">
        {guestsCount} {guestsCount === 1 ? 'гость' : guestsCount < 5 ? 'гостя' : 'гостей'}
      </p>

      <div className="counter__buttons">
        {/* ПОЧЕМУ? onClick передаёт ссылку на функцию, а не вызывает её сразу.
            Если написать onClick={increment()}, функция выполнится при рендере, а не при клике. */}
        <button onClick={decrement}>−</button>
        <button onClick={reset}>Сброс</button>
        <button onClick={increment}>+</button>
      </div>

      {/* ПОЧЕМУ? Дополнительные подсказки для пользователя улучшают UX
          и демонстрируют работу условного рендеринга в React. */}
      {guestsCount === 1 && <p>Начните с одного участника</p>}
      {guestsCount >= 10 && <p>⚠ Максимум 10 гостей для этого помещения</p>}
    </div>
  );
}